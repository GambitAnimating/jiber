import createGetRoom from './get-room'

////////////////////////////////////////////////////////////////////////////////
// mocks
////////////////////////////////////////////////////////////////////////////////
const getState: any = () => {
  return {
    rooms: {
      room1: 'hello'
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// setup
////////////////////////////////////////////////////////////////////////////////
const getRoom = createGetRoom(getState)

////////////////////////////////////////////////////////////////////////////////
// tests
////////////////////////////////////////////////////////////////////////////////
test('retrieve room state', () => {
  expect(getRoom('room1')).toBe('hello')
})

test('return null if room does not exist', () => {
  expect(getRoom('zala')).toBe(undefined)
})
